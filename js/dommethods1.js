function darkMode(){
    let element=document.getElementById("body-element")
    element.style.backgroundColor="black"
    element.style.color="grey"
}
function dayMode(){
    let element=document.getElementById("body-element")
    element.style.backgroundColor="white"
    element.style.color="black"
}
